package com.fedu.struc.src;

public class SwitchDemo {

	public static void main(String[] args) {
		
		int i=3;
		switch(i)
		{
		case 1:
			System.out.println("In case 1");
			break;
		case 2:
			System.out.println("In case 2");
			break;
			default:
				System.out.println("default case");
			
		}
	}
}
